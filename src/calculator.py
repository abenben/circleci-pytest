# -*- coding: utf-8 -*-

class Calculator(object):

    def addition(self, x, y):
        result = x + y;
        return result

    def subtraction(self, x, y):
        result = x - y;
        return result

    def multiplication(self, x, y):
        result = x * y;
        return result

    def division(self, x, y):
        result = x / y;
        return result
